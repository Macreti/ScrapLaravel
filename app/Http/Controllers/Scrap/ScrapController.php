    public function index()
    {
        $url = 'https://jabeas.com/';

        $ch = curl_init();

        curl_setopt_array($ch, array(
            CURLOPT_RETURNTRANSFER => 1,
            CURLOPT_URL => $url
        ));

        $response = curl_exec($ch);
        curl_close( $ch );

        return htmlspecialchars($response);
    }

